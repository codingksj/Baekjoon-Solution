bool CheckPrime(const int N) {
    if (N == 2 || N == 3) {
        return true;
    }

    if (N <= 1 || N % 2 == 0 || N % 3 == 0) {
        return false;
    }

    for (int i = 5; i * i <= N; i += 6){
        if (N % i == 0 || N % (i + 2) == 0) {
            return false;
        }
    }

    return true;
};

void DFS(const int num, const int digit, const int maxDigit) {
    int next;

    if (digit == maxDigit) {
        cout << num << "\n";
        return;
    }
    for (int i = 1; i <= 9; i++) {
        next = num * 10 + i;
        if (CheckPrime(next)) {
            DFS(next, digit + 1, maxDigit);
        }
    }
};