bool Input() {
    cin >> N;

    for (int i : Rng(0, N)) {
        cin >> A[i];
    }

    return true;
};

void Solve() {
    int mn = A[N - 1];
    sum = A[N - 1];
    for (int i : Rng(0, N - 1) | views::reverse) {
        if (A[i] > mn) {
            A[i] = mn;
        }
        else {
            mn = A[i];
        }
        sum += A[i];
    }
};

void Output() {
    println("{}", sum);
    return;
};
